print("Mencari perbandingan nilai")
variabel_a = int(input("Masukkan nilai variabel a : "))
variabel_b = int(input("Masukkan nilai variabel b : "))

if variabel_a > variabel_b:
    print("Nilai a lebih besar dari b")
elif variabel_a < variabel_b:
    print("Nilai b lebih besar dari a")
elif variabel_a >= variabel_b:
    print("Nilai a lebih besar sama dengan b")
elif variabel_a <= variabel_b:
    print("Nilai b lebih besar sama dengan a")


